    private void editPasswordSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editPasswordSubmitBtnActionPerformed
        String oldpass = editOldPasswordField.getText();
        String newpass1 = editNewPasswordField1.getText();
        String newpass2 = editNewPasswordField2.getText();
        if(!oldpass.equals("") && !newpass1.equals("") && !newpass2.equals("")){
            if(currentuser.getPassword().equals(oldpass)){
                if(!oldpass.equals(newpass1)){
                    System.out.println(oldpass);
                    System.out.println(newpass1);
                    if(newpass1.length() >= 6){
                        if(newpass1.equals(newpass2)){
                            JOptionPane.showMessageDialog(null, "Success! Password has been changed!");   
                            cypressSystem.modifyUserPassword(currentuser.getUserName(), newpass1);
                            currentuser = cypressSystem.getUser(currentuser.getUserName());
                            cardLayout.show(ui1Panel,"profilePanel");
                        }else{
                            editOldPasswordField.setText("");
                            editNewPasswordField1.setText("");
                            editNewPasswordField2.setText("");
                            JOptionPane.showMessageDialog(null, "New passwords do not match!");                    
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Password must be at least 6 characters!");
                    }
                }else{
                    editOldPasswordField.setText("");
                    editNewPasswordField1.setText("");
                    editNewPasswordField2.setText("");
                    JOptionPane.showMessageDialog(null, "New password cannot be same!");
                }
            }else{
                editOldPasswordField.setText("");
                editNewPasswordField1.setText("");
                editNewPasswordField2.setText("");
                JOptionPane.showMessageDialog(null, "Incorrect password!");
            }
        }else{
            editOldPasswordField.setText("");
            editNewPasswordField1.setText("");
            editNewPasswordField2.setText("");
            JOptionPane.showMessageDialog(null, "Please fill out all fields!");
        }
    }//GEN-LAST:event_editPasswordSubmitBtnActionPerformed

    private void liveMapBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_liveMapBtnActionPerformed
        ArrayList<PriorityStructure> markers = cypressSystem.getReports();
        ArrayList<String[]> list = new ArrayList<>();
        if (!markers.isEmpty()){
            for(PriorityStructure item: markers){
                String[] str = item.concern.split(" - ");
                String location = str[0];
                String concern = str[1];
                list.add(new String[]{location,concern});
            }
        }
        new LiveMap(list);
    }//GEN-LAST:event_liveMapBtnActionPerformed

