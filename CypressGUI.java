    private void createAccountBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccountBtnActionPerformed
        String fullname = "";
        String username = "";
        String password = "";
        String telephone = "";
        String address = "";
        String email = "";
        ArrayList<String> message = new ArrayList<String>();
        
        if(!firstnameField.getText().equals("") && !lastnameField.getText().equals("")){
            fullname = firstnameField.getText() + "" + 
                        lastnameField.getText();
        }else{
            message.add("Full name");
        }
        if(!usernameField.getText().equals("")){
            username = usernameField.getText();
        }else{
            message.add("Username");
        }
        if(!passwordField.getText().equals("")){
            password = passwordField.getText();
        }else{
            message.add("Password");
        }
        if(!addressField.getText().equals("")){
            address = addressField.getText();
        }else{
            message.add("Address");       
        }
        if(!emailField.getText().equals("")){
            email = emailField.getText();
        }else{
            message.add("Email");
        }
        if(!telephoneField.getText().equals("")){
            telephone = telephoneField.getText();
        }else{
            message.add("Telephone");
        }
        if(message.isEmpty()){
            if(password.length() >= 6){
                if(cypressSystem.createUserAccount(
                        fullname, username, password, telephone, address, email
                    )){
                    currentuser = cypressSystem.getUser(username);
                    JOptionPane.showMessageDialog(null, "Account created!");
                    cardLayout.show(ui1Panel, "mainPanel");
                    firstnameField.setText("");
                    lastnameField.setText("");
                    usernameField.setText("");
                    passwordField.setText("");
                    addressField.setText("");
                    emailField.setText("");
                    telephoneField.setText(""); 
                }else{
                    JOptionPane.showMessageDialog(null, "Username taken!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Password must be at least 6 characters!");
            }
        }else{
            String str = "Missing: ";
            for(int i = 0; i < message.size(); i++){
                str = str + message.get(i);
                if (i != message.size()-1){
                    str = str + ", ";
                }
            }
            JOptionPane.showMessageDialog(null, str);
        }

    }//GEN-LAST:event_createAccountBtnActionPerformed

