    private void submitReportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitReportBtnActionPerformed
        String address = reportAddressField.getText();
        String concern = (String)problemComboBox.getSelectedItem();
        if (!address.equals("")){
            if (problemComboBox.getSelectedIndex() != 0){
                reportAddressField.setText("");
                problemComboBox.setSelectedIndex(0);
                cypressSystem.submitReport(address,concern,currentuser.getUserName());
                JOptionPane.showMessageDialog(null, "Problem submitted!");
            }else{
                JOptionPane.showMessageDialog(null, "Please input concern!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please input address!");
        }
    }//GEN-LAST:event_submitReportBtnActionPerformed

    private void viewProfileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewProfileBtnActionPerformed
        profileNameLabel.setText(currentuser.getUserName());
        fullnameProfileLabel.setText(currentuser.getFullname());
        addressProfileLabel.setText(currentuser.getAddress());
        telephoneProfileLabel.setText(currentuser.getTeleNum());
        emailProfileLabel.setText(currentuser.getEmail());
        ArrayList<String> history = cypressSystem.getUserReports(currentuser.getUserName());
        if (!history.isEmpty()){
            for (String item : history){
            userReportList.add(item);
            }
        }else{
            userReportList.add("No Reports");
        }
        cardLayout.show(ui1Panel,"profilePanel");
    }//GEN-LAST:event_viewProfileBtnActionPerformed

