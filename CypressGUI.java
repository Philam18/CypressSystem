    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        if(loginUsernameField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter username!");
        }else if(loginPasswordField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter password!");
        }else{
            String username = loginUsernameField.getText();
            String password = loginPasswordField.getText();
            boolean valid = cypressSystem.loginUser(new String[]{username, password});
            if (valid == true){
                currentuser = cypressSystem.getUser(username);
                JOptionPane.showMessageDialog(null, "Success!");
                cardLayout.show(ui1Panel, "mainPanel");
                loginUsernameField.setText("");
                loginPasswordField.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Invalid info!");
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

